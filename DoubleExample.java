public class DoubleExample {
    public static void main(String[] args) {
        double pi = 3.14159265359, stock = 1254.75, gravity = 6.67430e-11, speed = 299792458.0, area = 4567.89876;
        System.out.println(pi + " " + stock + " " + gravity + " " + speed + " " + area);
        
        pi = 3.14; stock = 1350.25; gravity = 6.67400e-11; speed = 300000000.0; area = 5000.12345;
        System.out.println(pi + " " + stock + " " + gravity + " " + speed + " " + area);
    }
}