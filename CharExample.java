public class CharExample {
    public static void main(String[] args) {
        char grade = 'A', gender = 'M', currency = '$', letter = 'J', symbol = '@';
        System.out.println(grade + " " + gender + " " + currency + " " + letter + " " + symbol);
        
        grade = 'B'; gender = 'F'; currency = 'R'; letter = 'K'; symbol = '#';
        System.out.println(grade + " " + gender + " " + currency + " " + letter + " " + symbol);
    }
}